package assignments;

public class Program2_PrimitiveDataTypes {

	public static void main(String[] args) {
		
		byte b= 10;
		short s= 100;
		int val= 5000;
		long lng= 647672787487877L;
		float flo= 5.5F;
		double db= 999.99;
		char ch= 'Y';
		boolean bln = true;
		System.out.println("Byte value:" +b);
		System.out.println("Short value:" +s);
		System.out.println("Int value:" +val );
		System.out.println("Long value:" +lng);
		System.out.println("Float value:" +flo);
		System.out.println("Double value:" +db);
		System.out.println("Char value:" +ch);
		System.out.println("Boolen value:" +bln);
		
	}

}